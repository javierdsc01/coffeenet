    /**
     * Imprime la lista de cliente
     * @return String
     */
    public void leer() {
        Vector<String> clientesNuevos = new Vector();
        Vector<Cliente> clientes = (Vector<Cliente>) repoCliente.read();
        for(int i = 0; i<clientes.size(); i++){
            clientesNuevos.add(clientes.get(i).toStringPassword());
        }            
        jListCliente.setListData(clientesNuevos);
        //jListCliente.repaint();
    }

